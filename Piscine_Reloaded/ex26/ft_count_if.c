#include <stdlib.h>

int	ft_count_if(char **tab, int (*f)(char*))
{
	int k;

	k = 0;
	if (tab == NULL)
		return (0);
	while (*tab)
	{
		if (f(*tab++) == 1)
			k++;
	}
	return (k);
}
